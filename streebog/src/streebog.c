// Преобразование X из секции 6
// Аргументы:
//     a, b, out - блоки из 64 байт
// Возвращаемое значение:
//     out = a xor b
#define streebog_X(a, b, out, size) \
    do { \
        for (int definitely_not_a_collision_iterator_name = 0; definitely_not_a_collision_iterator_name < size; definitely_not_a_collision_iterator_name++) { \
            out[definitely_not_a_collision_iterator_name] = a[definitely_not_a_collision_iterator_name] ^ b[definitely_not_a_collision_iterator_name]; \
        } \
    } while (0)

// Преобразование S из секции 6
// Аргументы:
//     a - блок из 64 байт
// Возвращаемое значение:
//     S(a)
#define streebog_S(a, size) \
    do { \
        for (int definitely_not_a_collision_iterator_name = 0; definitely_not_a_collision_iterator_name < size; definitely_not_a_collision_iterator_name++) { \
            a[definitely_not_a_collision_iterator_name] = streebog_Pi[a[definitely_not_a_collision_iterator_name]]; \
        } \
    } while (0)

// Преобразование P из секции 6
// Аргументы:
//     a - блок из 64 байт
// Возвращаемое значение:
//     P(a)
void streebog_P(streebog_block_t a, size_t size)
{
    streebog_block_t tmp;
    for (int i = 63; i >= 0; i--) {
        tmp[i] = a[streebog_Tau[i]];
    }
    memcpy(a, tmp, size);
}

// Преобразование L из секции 6
// Аргументы:
//     a - блок из 64 байт
// Возвращаемое значение:
//     L(a)
void streebog_L(streebog_block_t a)
{
    uint64_t *in = (uint64_t *)a;
    uint64_t out[8];
    memset(out, 0, 8 * sizeof(uint64_t));

    for (int i = 7; i >= 0; i--) {
        for (int j = 63; j >= 0; j--) {
            if ((in[i] >> j) & 1) {
                out[i] ^= streebog_A[63-j];
            }
        }
    }
    memcpy(a, out, STREEBOG_BLOCK_SIZE);
}

// Сложение чисел по модулю 2^{512}
// Аргументы:
//     a, b, out - блоки из 64 байт
// Возвращаемое значение:
//     out = a + b
void streebog_Add_mod512(streebog_block_t a, streebog_block_t b, streebog_block_t out)
{
    int internal = 0;
    for (int i = 0; i < 64; i++)
    {
        internal = a[i] + b[i] + (internal >> 8);
        out[i] = internal & 0xff;
    }
}

// Очистка буфера путем перезаписи его случайными значениями
// Аргументы:
//     buf - указатель на буфер
//     size - размер буфера
// Возвращаемое значение:
//     buf перезаписывается случайными значениями
__attribute__((optimize(0))) void streebog_clear_buf(uint8_t *buf, ssize_t size)
{
    if (getrandom(buf, size, 0) != size) {
        perror("getrandom");
        exit(1);
    }
}

// Функция E из секции 7
// Аргументы:
//     K - блок из 64 байт
//     m - блок из 64 байт
//     out - блок из 64 байт
// Возвращаемое значение:
//     out = E(K, m)
void streebog_E(streebog_block_t K, const streebog_block_t m, streebog_block_t out)
{
    // K_1 = K
    // out = X[K_1](m)
    streebog_X(K, m, out, STREEBOG_BLOCK_SIZE);
    // X[K_i]LPS(out)
    for (int i = 0; i < 12; i++) {
        streebog_S(out, STREEBOG_BLOCK_SIZE);
        streebog_P(out, STREEBOG_BLOCK_SIZE);
        streebog_L(out);

        // K = K_{i-1} xor C_{i-1}
        streebog_X(K, streebog_C[i], K, STREEBOG_BLOCK_SIZE);
        // K_i = LPS(K_{i-1} xor C_{i-1})
        streebog_S(K, STREEBOG_BLOCK_SIZE);
        streebog_P(K, STREEBOG_BLOCK_SIZE);
        streebog_L(K);

        streebog_X(out, K, out, STREEBOG_BLOCK_SIZE);
    }
}

// Функция g из секции 7
// Аргументы:
//     h - блок из 64 байт
//     m - блок из 64 байт
//     N - блок из 64 байт
// Возвращаемое значение:
//     h = g_N(h, m)
void streebog_g(streebog_block_t h, const streebog_block_t m, streebog_block_t N)
{
    streebog_block_t K, tmp;

    // K = h xor N
    streebog_X(h, N, K, STREEBOG_BLOCK_SIZE);
    // LPS(K)
    streebog_S(K, STREEBOG_BLOCK_SIZE);
    streebog_P(K, STREEBOG_BLOCK_SIZE);
    streebog_L(K);
    // E(K, m) xor h xor m
    streebog_E(K, m, tmp);
    streebog_X(tmp, h, tmp, STREEBOG_BLOCK_SIZE);
    streebog_X(tmp, m, h, STREEBOG_BLOCK_SIZE);
}

