// –§—É–Ω–∫—Ü–∏—è E –∏–∑ —Å–µ–∫—Ü–∏–∏ 7
// –ê—Ä–≥—É–º–µ–Ω—Ç—ã:
//     K - –±–ª–æ–∫ –∏–∑ 64 –±–∞–π—Ç
//     m - –±–ª–æ–∫ –∏–∑ 64 –±–∞–π—Ç
//     out - –±–ª–æ–∫ –∏–∑ 64 –±–∞–π—Ç
// –í–æ–∑–≤—Ä–∞—â–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ:
//     out = E(K, m)
void streebog_E(streebog_block_t K, const streebog_block_t m, streebog_block_t out)
{
    // K_1 = K
    // out = X[K_1](m)
    streebog_X(K, m, out, STREEBOG_BLOCK_SIZE);
    // X[K_i]LPS(out)
    for (int i = 0; i < 12; i++)
    {
        streebog_S(out, STREEBOG_BLOCK_SIZE);
        streebog_P(out, STREEBOG_BLOCK_SIZE);
        streebog_L(out);

        // K = K_{i-1} xor C_{i-1}
        streebog_X(K, streebog_C[i], K, STREEBOG_BLOCK_SIZE);
        // K_i = LPS(K_{i-1} xor C_{i-1})
        streebog_S(K, STREEBOG_BLOCK_SIZE);
        streebog_P(K, STREEBOG_BLOCK_SIZE);
        streebog_L(K);

        streebog_X(out, K, out, STREEBOG_BLOCK_SIZE);
    }
}

// –§—É–Ω–∫—Ü–∏—è g –∏–∑ —Å–µ–∫—Ü–∏–∏ 7
// –ê—Ä–≥—É–º–µ–Ω—Ç—ã:
//     h - –±–ª–æ–∫ –∏–∑ 64 –±–∞–π—Ç
//     m - –±–ª–æ–∫ –∏–∑ 64 –±–∞–π—Ç
//     N - –±–ª–æ–∫ –∏–∑ 64 –±–∞–π—Ç
// –í–æ–∑–≤—Ä–∞—â–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ:
//     h = g_N(h, m)
void streebog_g(streebog_block_t h, const streebog_block_t m, streebog_block_t N)
{
    streebog_block_t K, tmp;

    // K = h xor N
    streebog_X(h, N, K, STREEBOG_BLOCK_SIZE);
    // LPS(K)
    streebog_S(K, STREEBOG_BLOCK_SIZE);
    streebog_P(K, STREEBOG_BLOCK_SIZE);
    streebog_L(K);
    // E(K, m) xor h xor m
    streebog_E(K, m, tmp);
    streebog_X(tmp, h, tmp, STREEBOG_BLOCK_SIZE);
    streebog_X(tmp, m, h, STREEBOG_BLOCK_SIZE);
}

