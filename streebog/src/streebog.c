// –≠—Ç–∞–ø 2 –∏–∑ —Å–µ–∫—Ü–∏–∏ 8.2
// –ê—Ä–≥—É–º–µ–Ω—Ç—ã:
//     sb - —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä—É Streebog
//     m - –±–ª–æ–∫ –¥–∞–Ω–Ω—ã—Ö
void streebog_stage2(Streebog *sb, streebog_block_t m)
{
    // –®–∞–≥ 2.3
    streebog_g(sb->h, m, sb->N);
    // –®–∞–≥ 2.4
    streebog_Add_mod512(sb->N, value_512, sb->N);
    // –®–∞–≥ 2.5
    streebog_Add_mod512(sb->Sigma, m, sb->Sigma);
}

// –≠—Ç–∞–ø 3 –∏–∑ —Å–µ–∫—Ü–∏–∏ 8.3
// –ê—Ä–≥—É–º–µ–Ω—Ç—ã:
//     sb - —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä—É Streebog
//     m - –±–ª–æ–∫ –¥–∞–Ω–Ω—ã—Ö
//     size - —Ä–∞–∑–º–µ—Ä –±–ª–æ–∫–∞ –¥–∞–Ω–Ω—ã—Ö
// –í–æ–∑–≤—Ä–∞—â–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ:
//     sb->Sigma
void streebog_stage3(Streebog *sb, streebog_block_t m, size_t size)
{
    // –®–∞–≥ 3.1
    streebog_block_t tmp;
    memset(tmp, 0, STREEBOG_BLOCK_SIZE);
    memcpy(tmp, m, size);
    tmp[size] = 0x01;
    memcpy(m, tmp, STREEBOG_BLOCK_SIZE);
    // –®–∞–≥ 3.2
    streebog_g(sb->h, m, sb->N);
    // –®–∞–≥ 3.3
    memset(tmp, 0, STREEBOG_BLOCK_SIZE);
    tmp[1] = ((size * 8) >> 8) & 0xff;
    tmp[0] = (size * 8) & 0xff;
    streebog_Add_mod512(sb->N, tmp, sb->N);
    // –®–∞–≥ 3.4
    streebog_Add_mod512(sb->Sigma, m, sb->Sigma);
    // –®–∞–≥ 3.5
    streebog_g(sb->h, sb->N, value_0);
    // –®–∞–≥ 3.6
    streebog_g(sb->h, sb->Sigma, value_0);
}

// –•—ç—à–∏—Ä–æ–≤–∞–Ω–∏–µ —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ —Ñ–∞–π–ª–∞
// –ê—Ä–≥—É–º–µ–Ω—Ç—ã:
//     sb - —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä—É Streebog
//     f_in - —Ñ–∞–π–ª, —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ –∫–æ—Ç–æ—Ä–æ–≥–æ —Ö—ç—à–∏—Ä—É–µ—Ç—Å—è
//     f_out - —Ñ–∞–π–ª, –≤ –∫–æ—Ç–æ—Ä—ã–π –∑–∞–ø–∏—Å—ã–≤–∞–µ—Ç—Å—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Ö—ç—à–∏—Ä–æ–≤–∞–Ω–∏—è
void streebog_hash_file(Streebog *sb, FILE *f_in, FILE *f_out)
{
    uint8_t buf[STREEBOG_BLOCK_SIZE];
    while (1)
    {
        size_t bytes_read = fread(buf, 1, STREEBOG_BLOCK_SIZE, f_in);
        if (bytes_read == STREEBOG_BLOCK_SIZE)
        {
            streebog_stage2(sb, buf);
        }
        else
        {
            streebog_stage3(sb, buf, bytes_read);
            break;
        }
    }
#ifdef STREEBOG_512
    fwrite(sb->h, 1, STREEBOG_BLOCK_SIZE, f_out);
#else
    fwrite(&sb->h[32], 1, 32, f_out);
#endif
}

void streebog_hash_array(Streebog *sb, uint8_t *array, size_t len, uint8_t *out)
{
    uint8_t buf[STREEBOG_BLOCK_SIZE];
    size_t bytes_left = len;
    while (bytes_left > 63)
    {
        memcpy(buf, array, STREEBOG_BLOCK_SIZE);
        streebog_stage2(sb, buf);
        array += STREEBOG_BLOCK_SIZE;
        bytes_left -= STREEBOG_BLOCK_SIZE;
    }
    memcpy(buf, array, bytes_left);
    streebog_stage3(sb, buf, bytes_left);
#ifdef STREEBOG_512
    memcpy(out, sb->h, STREEBOG_BLOCK_SIZE);
#else
    memcpy(out, &sb->h[32], 32);
#endif
}

/**
 * @brief –í—ã—á–∏—Å–ª—è–µ—Ç –∫–æ–¥ –∞—É—Ç–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ü–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏—è HMAC –Ω–∞ –æ—Å–Ω–æ–≤–µ —Ö—ç—à —Ñ—É–Ω–∫—Ü–∏–∏ –ì–û–°–¢ –† 34.11-2012 256 –±–∏—Ç
 *
 * @param key –∫–ª—é—á (K)
 * @param key_len –¥–ª–∏–Ω–∞ –∫–ª—é—á–∞ –≤ –±–∏—Ç–∞—Ö (n)
 * @param data –≤—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ (T)
 * @param data_len –¥–ª–∏–Ω–∞ –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
 * @param out –±—É—Ñ—Ñ–µ—Ä –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–π –Ω–∞–¥ K* –∏ T —Å –ø–æ–º–æ—â—å—é –ì–û–°–¢ –† 34.11-2012 256 –±–∏—Ç
 */
void streebog_hmac_256(const uint8_t *key, size_t key_len, const uint8_t *data, size_t data_len, hmac_block_t out)
{
    Streebog sb = {0};
    streebog_new(&sb);

    uint8_t k_star[64];
    // K_star = (0, ..., 0) \in V_512
    memset(k_star, 0, 64);
    // K_star = (K, ..., 0) \in V_512
    size_t key_len_bytes = key_len % 8 > 0 ? key_len / 8 + 1 : key_len / 8;
    memcpy(k_star, key, key_len_bytes);

    size_t tail = data_len > 32 ? data_len : 32;
    uint8_t *tmp = (uint8_t *)malloc((64 + tail) * sizeof(uint8_t));
    memset(tmp, 0, 64 + tail);
    // tmp = K* xor ipad
    streebog_X(k_star, ipad, tmp, 64);
    // tmp = (K* xor ipad) || T
    memcpy(tmp + 64, data, data_len);

    uint8_t hash[32];
    memset(hash, 0, 32);
    // hash = H_256((K* xor ipad) || T)
    streebog_hash_array(&sb, tmp, 64 + data_len, hash);

    memset(tmp, 0, 64 + tail);
    // tmp = K* xor opad
    streebog_X(k_star, opad, tmp, 64);
    // tmp = (K* xor opad) || hash
    memcpy(tmp + 64, hash, 32);
    // out = H_256((K* xor opad) || hash)

    // –∫—Ç–æ –∂–µ –∑–Ω–∞–ª, —á—Ç–æ –Ω—É–∂–Ω–æ —Å–±—Ä–æ—Å–∏—Ç—å –∑–Ω–∞—á–µ–Ω–∏–µ sb.h...
    // —Å–º–∞–π–ª–∏ —Ñ–µ–π—Å)üòäüòäüòä
    streebog_reset(&sb);
    streebog_hash_array(&sb, tmp, 64 + 32, out);

    streebog_clear_buf(tmp, 64 + tail);
    free(tmp);
}