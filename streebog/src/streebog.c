// Этап 2 из секции 8.2
// Аргументы:
//     sb - указатель на структуру Streebog
//     m - блок данных
void streebog_stage2(Streebog *sb, streebog_block_t m)
{
    // Шаг 2.3
    streebog_g(sb->h, m, sb->N);
    // Шаг 2.4
    streebog_Add_mod512(sb->N, value_512, sb->N);
    // Шаг 2.5
    streebog_Add_mod512(sb->Sigma, m, sb->Sigma);
}

// Этап 3 из секции 8.3
// Аргументы:
//     sb - указатель на структуру Streebog
//     m - блок данных
//     size - размер блока данных
// Возвращаемое значение:
//     sb->Sigma
void streebog_stage3(Streebog *sb, streebog_block_t m, size_t size)
{
    // Шаг 3.1
    streebog_block_t tmp;
    memset(tmp, 0, STREEBOG_BLOCK_SIZE);
    memcpy(tmp, m, size);
    tmp[size] = 0x01;
    memcpy(m, tmp, STREEBOG_BLOCK_SIZE);
    // Шаг 3.2
    streebog_g(sb->h, m, sb->N);
    // Шаг 3.3
    memset(tmp, 0, STREEBOG_BLOCK_SIZE);
    tmp[1] = ((size * 8) >> 8) & 0xff;
    tmp[0] = (size * 8) & 0xff;
    streebog_Add_mod512(sb->N, tmp, sb->N);
    // Шаг 3.4
    streebog_Add_mod512(sb->Sigma, m, sb->Sigma);
    // Шаг 3.5
    streebog_g(sb->h, sb->N, value_0);
    // Шаг 3.6 для 512 бит
    streebog_g(sb->h, sb->Sigma, value_0);
}

// Хэширование содержимого файла
// Аргументы:
//     sb - указатель на структуру Streebog
//     f_in - файл, содержимое которого хэшируется
//     f_out - файл, в который записывается результат хэширования
void streebog_hash_file(Streebog *sb, FILE *f_in, FILE *f_out)
{
    uint8_t buf[STREEBOG_BLOCK_SIZE];
    while (1) {
        size_t bytes_read = fread(buf, 1, STREEBOG_BLOCK_SIZE, f_in);
        if (bytes_read == STREEBOG_BLOCK_SIZE) {
            streebog_stage2(sb, buf);
        } else {
            streebog_stage3(sb, buf, bytes_read);
            break;
        }
    }
    #ifdef STREEBOG_512
    fwrite(sb->h, 1, STREEBOG_BLOCK_SIZE, f_out);
    #else
    fwrite(&sb->h[32], 1, 32, f_out);
    #endif
}

void streebog_hash_array(Streebog *sb, uint8_t *array, size_t size, streebog_block_t out)
{
    uint8_t buf[STREEBOG_BLOCK_SIZE];
    size_t bytes_read = size;
    while (bytes_read > 63) {
        memcpy(buf, array, STREEBOG_BLOCK_SIZE);
        streebog_stage2(sb, buf);
        array += STREEBOG_BLOCK_SIZE;
        bytes_read -= STREEBOG_BLOCK_SIZE;
    }
    memcpy(buf, array, bytes_read);
    streebog_stage3(sb, buf, bytes_read);
    #ifdef STREEBOG_512
    memcpy(out, sb->h, STREEBOG_BLOCK_SIZE);
    #else
    memcpy(out, &sb->h[32], 32);
    #endif
}